package lv.javaguru.demo.lecture2;

public class MyCalculator {
    public static void main(String[] args) {
        int firstNumber = 30;
        int secondNumber = 10;
        int numberSum = firstNumber + secondNumber;
        int numberSub = firstNumber - secondNumber;
        int numberMultiply = firstNumber * secondNumber;
        int numberDivide = firstNumber / secondNumber;
        System.out.println("Sum is " + numberSum);
        System.out.println("Subtract is " + numberSub);
        System.out.println("Multiply is " + numberMultiply);
        System.out.println("Division is " + numberDivide);
    }
}
